let currentTrackingProductId = null;

async function loadTrackingMap(targetWh = null, targetRack = null, targetShelf = null) {
    const wh = document.getElementById('track-wh').value;
    try {
        const res = await fetch(`/api/tracking/map?warehouse=${encodeURIComponent(wh)}`);
        const data = await res.json();
        const container = document.getElementById('map-container');
        container.innerHTML = '';
        if (data.success) {
            let warehouses = data.map_data;
            if (Object.keys(warehouses).length === 0) {
                if (wh !== 'All Warehouses' && wh.trim() !== '') {
                    warehouses = { [wh]: {} };
                } else {
                    container.innerHTML = '<h3 style="color:#64748B;">No layout data available</h3>';
                    return;
                }
            }
            for (const wName in warehouses) {
                const whDiv = document.createElement('div');
                whDiv.className = 'warehouse-block';
                whDiv.innerHTML = `<div class="wh-title">Warehouse: ${wName}</div>`;
                const racksGrid = document.createElement('div');
                racksGrid.className = 'racks-grid';
                const racks = warehouses[wName] || {};
                
                // Enforce 20 racks
                for (let r = 1; r <= 20; r++) {
                    const rName = r.toString();
                    const rackDiv = document.createElement('div');
                    const isTargetRack = (targetWh === wName && targetRack === rName);
                    rackDiv.className = `rack-card ${isTargetRack ? 'target' : ''}`;
                    rackDiv.innerHTML = `<div class="rack-header">${isTargetRack ? '📍 ' : ''}Rack ${rName}</div>`;
                    
                    const shelvesContainer = document.createElement('div');
                    shelvesContainer.className = 'shelves-container';
                    const shelves = racks[rName] || {};
                    
                    // Enforce 7 shelves per rack
                    for (let s = 1; s <= 7; s++) {
                        const sName = s.toString();
                        const prods = shelves[sName] || [];

                        // Determine if this shelf has the searched product
                        let hasSearchedProduct = false;
                        if (currentTrackingProductId) {
                            hasSearchedProduct = prods.some(p => p.id == currentTrackingProductId);
                        }

                        const isLowStock = prods.some(p => p.qty < 20);
                        const isTarget = (targetWh === wName && targetRack === rName && targetShelf === sName);

                        let sClass = 'shelf-box';
                        if (isTarget || (currentTrackingProductId && hasSearchedProduct)) sClass += ' target-shelf'; // slight highlight

                        const sDiv = document.createElement('div');
                        sDiv.className = sClass;
                        
                        const sTitle = document.createElement('div');
                        sTitle.className = 'shelf-title';
                        sTitle.innerText = `Shelf ${sName}`;
                        sDiv.appendChild(sTitle);
                        
                        // Create 50 pallet grid
                        const palletGrid = document.createElement('div');
                        palletGrid.className = 'pallet-grid';
                        
                        // Map products to pallets. If bin_pallet_no is a number between 1 and 50, put it there.
                        const palletMap = {};
                        const unassigned = [];
                        prods.forEach(p => {
                            let slotFound = false;
                            if (p.bin_pallet_no) {
                                let num = parseInt(p.bin_pallet_no.replace(/\D/g, ''), 10);
                                if (!isNaN(num) && num >= 1 && num <= 50) {
                                    if (!palletMap[num]) palletMap[num] = [];
                                    palletMap[num].push(p);
                                    slotFound = true;
                                }
                            }
                            if (!slotFound) unassigned.push(p);
                        });

                        for (let pIdx = 1; pIdx <= 50; pIdx++) {
                            const slotDiv = document.createElement('div');
                            slotDiv.className = 'pallet-slot';
                            
                            let slotProds = palletMap[pIdx] || [];
                            if (slotProds.length === 0 && unassigned.length > 0) {
                                slotProds.push(unassigned.shift());
                            }
                            
                            if (slotProds.length > 0) {
                                slotDiv.classList.add('occupied');
                                
                                // Highlight logic for specific pallet
                                let isSlotTarget = false;
                                if (currentTrackingProductId && slotProds.some(p => p.id == currentTrackingProductId)) {
                                    isSlotTarget = true;
                                }
                                if (isSlotTarget) slotDiv.classList.add('target');
                                
                                const tt = document.createElement('span');
                                tt.className = 'tooltiptext';
                                let ttLines = [`Warehouse: ${wName}`, `Rack ${rName}, Shelf ${sName}, Pallet ${pIdx}`];
                                slotProds.forEach(p => {
                                    let bp_info = p.bin_pallet ? ` [BP: ${p.bin_pallet}]` : "";
                                    ttLines.push(`${p.name} (ID: ${p.id}) - Qty: ${p.qty}${bp_info}`);
                                });
                                tt.innerText = ttLines.join('\n');
                                slotDiv.appendChild(tt);
                            } else {
                                // empty slot tooltip
                                const tt = document.createElement('span');
                                tt.className = 'tooltiptext';
                                tt.innerText = `Rack ${rName}, Shelf ${sName}\nPallet ${pIdx} (Empty)`;
                                slotDiv.appendChild(tt);
                            }
                            
                            palletGrid.appendChild(slotDiv);
                        }
                        
                        sDiv.appendChild(palletGrid);
                        shelvesContainer.appendChild(sDiv);
                    }
                    
                    rackDiv.appendChild(shelvesContainer);
                    racksGrid.appendChild(rackDiv);
                }
                whDiv.appendChild(racksGrid);
                container.appendChild(whDiv);
            }
        }
    } catch (e) {
        console.error(e);
    }
}


