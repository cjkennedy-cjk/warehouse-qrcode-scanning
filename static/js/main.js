function processFile() {
    const fileInput = document.getElementById('apiFile');
    const file = fileInput.files[0];
    
    if (!file) {
        alert("Please select a file first.");
        return;
    }

    const reader = new FileReader();
    reader.onload = async function(e) {
        try {
            const content = e.target.result;
            let jsonData;
            
            try {
                jsonData = JSON.parse(content);
            } catch (err) {
                alert("Invalid JSON format. Please ensure the API file is a valid JSON.");
                return;
            }

            // Attempt to find the array of items if the JSON is an object
            let itemsArray = [];
            if (Array.isArray(jsonData)) {
                itemsArray = jsonData;
            } else {
                // Look for the first key that contains an array
                for (let key in jsonData) {
                    if (Array.isArray(jsonData[key])) {
                        itemsArray = jsonData[key];
                        break;
                    }
                }
                // If no array found, just wrap the object in an array
                if (itemsArray.length === 0) {
                    itemsArray = [jsonData];
                }
            }

            globalProcessedData = itemsArray;
            populateProductList();

            // Save data to server for persistence
            try {
                const res = await fetch('/api/save-api-data', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify(itemsArray)
                });
                const saveResult = await res.json();
                if (!saveResult.success) {
                    console.error("Failed to save data on server.");
                }
            } catch (err) {
                console.error("Error saving data:", err);
            }
            
        } catch (error) {
            console.error("Error processing file:", error);
            alert("An error occurred while processing the file.");
        }
    };
    reader.readAsText(file);
}

async function fetchAndProcessAPI() {
    const urlInput = document.getElementById('apiUrl').value.trim();
    if (!urlInput) {
        alert('Please enter a valid API URL.');
        return;
    }

    try {
        const response = await fetch('/api/fetch-and-save-link', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ api_url: urlInput })
        });

        const result = await response.json();

        if (result.success) {
            let jsonData = result.data;
            let itemsArray = [];
            if (Array.isArray(jsonData)) {
                itemsArray = jsonData;
            } else {
                for (let key in jsonData) {
                    if (Array.isArray(jsonData[key])) {
                        itemsArray = jsonData[key];
                        break;
                    }
                }
                if (itemsArray.length === 0) {
                    itemsArray = [jsonData];
                }
            }

            globalProcessedData = itemsArray;
            populateProductList();

            try {
                const res = await fetch('/api/save-api-data', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify(itemsArray)
                });
                const saveResult = await res.json();
                if (!saveResult.success) {
                    console.error("Failed to save data on server.");
                }
            } catch (err) {
                console.error("Error saving data:", err);
            }
            alert('Data fetched and processed successfully!');
        } else {
            alert('Error: ' + result.message);
        }
    } catch (err) {
        alert('Request failed: ' + err.message);
    }
}

