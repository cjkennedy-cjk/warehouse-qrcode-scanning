function applyFilters() {
    const cat = document.getElementById('inv-cat').value;
    const wh = document.getElementById('inv-wh').value;
    const exp = document.getElementById('inv-exp').value;
    const search = document.getElementById('inv-search').value.toLowerCase().trim();
    
    const todayStr = new Date().toISOString().split('T')[0];
    let soonDate = new Date();
    soonDate.setDate(soonDate.getDate() + 30);
    const soonStr = soonDate.toISOString().split('T')[0];
    
    let totalQty = 0;
    let expiredProds = 0;
    let expiringProds = 0;
    
    const filtered = inventoryData.filter(r => {
        if (cat !== "All Categories" && r.category !== cat) return false;
        if (wh !== "All Warehouses" && r.warehouse_name !== wh) return false;
        
        let isExpired = false;
        let isExpiringSoon = false;
        const eStr = (r.expiry_date || "").trim();
        
        if (eStr && eStr !== "YYYY-MM-DD" && eStr !== "--") {
            if (eStr < todayStr) isExpired = true;
            else if (eStr <= soonStr) isExpiringSoon = true;
        }
        
        if (exp === "Expired" && !isExpired) return false;
        if (exp === "Expiring within 30 days" && !isExpiringSoon) return false;
        if (exp === "Valid" && (isExpired || isExpiringSoon)) return false;
        
        if (search) {
            const match = [r.product_id, r.name, r.category, r.brand, r.warehouse_name, r.batch_no].some(v => 
                v && v.toString().toLowerCase().includes(search)
            );
            if (!match) return false;
        }
        
        // Track stats for cards
        totalQty += parseInt(r.qty) || 0;
        if (isExpired) expiredProds++;
        if (isExpiringSoon) expiringProds++;
        
        // Store computed status for rendering
        r._isExpired = isExpired;
        r._isExpiringSoon = isExpiringSoon;
        
        return true;
    });
    
    currentFilteredInventory = filtered;
    
    // Update Cards
    document.getElementById('card-total-prod').innerText = filtered.length.toLocaleString();
    document.getElementById('card-total-qty').innerText = totalQty.toLocaleString();
    document.getElementById('card-expired').innerText = expiredProds.toLocaleString();
    document.getElementById('card-expiring').innerText = expiringProds.toLocaleString();
    
    // Render Table
    const tbody = document.getElementById('inv-tbody');
    tbody.innerHTML = '';
    
    filtered.forEach((r, idx) => {
        let status = "Valid";
        let badgeClass = "valid";
        
        if (r._isExpired) {
            status = "Expired"; badgeClass = "expired";
        } else if (r._isExpiringSoon) {
            status = "Expiring Soon"; badgeClass = "expiring";
        } else if ((r.qty || 0) < 20) {
            status = "Low Stock"; badgeClass = "low";
        }
        
        let bp = `${r.bin_pallet || ""} ${r.bin_pallet_no || ""}`.trim();
        const tr = document.createElement('tr');
        tr.innerHTML = `
            <td>${idx + 1}</td>
            <td>${r.product_id}</td>
            <td>${r.name}</td>
            <td>${r.category || "--"}</td>
            <td>${r.brand || "--"}</td>
            <td>${r.warehouse_name || "--"}</td>
            <td>${r.rack || "--"}</td>
            <td>${r.shelf || "--"}</td>
            <td>${bp || "--"}</td>
            <td>${r.batch_no || "--"}</td>
            <td class="bold">${r.qty}</td>
            <td>${r.expiry_date || "--"}</td>
            <td><span class="badge ${badgeClass}">${status}</span></td>
            <td><button class="btn btn-red" style="padding:4px 8px; font-size:0.8rem;" onclick="deleteInventory(${r.id}, '${r.name}')">Delete</button></td>
        `;
        tbody.appendChild(tr);
    });
}

