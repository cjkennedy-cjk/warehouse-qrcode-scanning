let html5QrCode;
let lastScannedCode = "";

/**
 * QR codes generated by this app embed a full text block:
 *   "Product ID: M0001\nName: Bedsheet\n..."
 * This function extracts just the Product ID from that text.
 * If the text is a plain ID (no newlines), it is returned as-is.
 */
function parseQrData(rawText) {
    const lines = rawText.split('\n');
    for (const line of lines) {
        const match = line.match(/^Product ID\s*:\s*(.+)/i);
        if (match) {
            return match[1].trim();
        }
    }
    // Fallback: use the raw text (plain barcode / simple ID)
    return rawText.trim();
}

function onScanSuccess(decodedText, decodedResult) {
    const productId = parseQrData(decodedText);
    if (productId !== lastScannedCode) {
        lastScannedCode = productId;
        lookupProduct(productId);
    }
}

function onScanFailure(error) {
    // handle scan failure, usually better to ignore and keep scanning
}

function startScanner() {
    if (!html5QrCode) {
        html5QrCode = new Html5Qrcode("reader");
    }
    const config = { fps: 10, qrbox: { width: 250, height: 250 } };
    
    // Attempt to use the rear camera first
    html5QrCode.start({ facingMode: "environment" }, config, onScanSuccess, onScanFailure)
        .then(() => {
            document.getElementById('scanner-status').innerText = "STATUS: SCANNING LIVE...";
            document.getElementById('scanner-status').style.backgroundColor = "#10B981";
        })
        .catch(err => {
            // If environment camera fails, fallback to user (front) camera
            html5QrCode.start({ facingMode: "user" }, config, onScanSuccess, onScanFailure)
                .then(() => {
                    document.getElementById('scanner-status').innerText = "STATUS: SCANNING LIVE...";
                    document.getElementById('scanner-status').style.backgroundColor = "#10B981";
                })
                .catch(err2 => {
                    if (window.isSecureContext === false) {
                        alert(`Camera access is blocked by your browser. Please access the site via HTTPS or localhost (or use the image upload option).`);
                    } else {
                        alert(`Error starting scanner: No suitable camera found or permission denied.`);
                    }
                });
        });
}

function scanUploadedFile(event) {
    if (event.target.files.length === 0) return;
    const file = event.target.files[0];

    const statusEl = document.getElementById('scanner-status');
    statusEl.innerText = "STATUS: READING UPLOADED IMAGE...";
    statusEl.style.backgroundColor = "#F59E0B";

    // Use a dedicated off-screen div for file scanning so it
    // works whether or not the live camera scanner is running.
    let uploadDiv = document.getElementById('reader-upload-hidden');
    if (!uploadDiv) {
        uploadDiv = document.createElement('div');
        uploadDiv.id = 'reader-upload-hidden';
        uploadDiv.style.display = 'none';
        document.body.appendChild(uploadDiv);
    }

    const fileScanner = new Html5Qrcode('reader-upload-hidden');
    fileScanner.scanFile(file, /* showImage= */ false)
        .then(decodedText => {
            // Parse the QR payload to get just the Product ID,
            // then always trigger a fresh lookup regardless of last scan.
            const productId = parseQrData(decodedText);
            lastScannedCode = productId;
            lookupProduct(productId);
        })
        .catch(err => {
            statusEl.innerText = "STATUS: NO QR CODE DETECTED IN IMAGE";
            statusEl.style.backgroundColor = "#EF4444";
            console.error('Upload scan error:', err);
        })
        .finally(() => {
            // Reset file input so the same file can be re-uploaded
            event.target.value = "";
        });
}

function stopScanner() {
    if (html5QrCode && html5QrCode.isScanning) {
        html5QrCode.stop().then(() => {
            document.getElementById('scanner-status').innerText = "STATUS: INACTIVE";
            document.getElementById('scanner-status').style.backgroundColor = "#EF4444";
        }).catch(err => {
            console.error("Failed to stop scanning.", err);
        });
    }
}

let lastProductData = null;

async function lookupProduct(productId) {
    const statusEl = document.getElementById('scanner-status');
    statusEl.innerText = "STATUS: SEARCHING DATABASE...";
    statusEl.style.backgroundColor = "#3B82F6";

    let res, data;
    try {
        res = await fetch(`/api/product/${encodeURIComponent(productId)}`, {
            credentials: 'same-origin'
        });
    } catch (networkErr) {
        statusEl.innerText = `NETWORK ERROR: ${networkErr.message}`;
        statusEl.style.backgroundColor = "#EF4444";
        console.error('Network error in lookupProduct:', networkErr);
        return;
    }

    // Session expired — redirect back to login
    if (res.status === 401) {
        statusEl.innerText = "SESSION EXPIRED — Redirecting to login...";
        statusEl.style.backgroundColor = "#F59E0B";
        setTimeout(() => { window.location.href = '/'; }, 1500);
        return;
    }

    try {
        data = await res.json();
    } catch (jsonErr) {
        statusEl.innerText = `SERVER RESPONSE ERROR (status ${res.status})`;
        statusEl.style.backgroundColor = "#EF4444";
        console.error('JSON parse error:', jsonErr);
        return;
    }

    if (data.success) {
        const p = data.data;
        lastProductData = p;
        
        document.getElementById('det-pid').innerText = p.product_id;
        document.getElementById('det-name').innerText = p.name;
        document.getElementById('det-cat').innerText = p.category;
        document.getElementById('det-brand').innerText = p.brand;
        document.getElementById('det-batch').innerText = p.batch_no;
        document.getElementById('det-expiry').innerText = p.expiry_date;
        document.getElementById('det-total-qty').innerText = p.total_qty;
        document.getElementById('det-balance-qty').innerText = p.balance_qty;
        document.getElementById('det-qr').innerText = p.barcode;
        
        const tbody = document.getElementById('det-wh-body');
        tbody.innerHTML = '';
        if (p.warehouses.length > 0) {
            p.warehouses.forEach(w => {
                let bp = `${w.bin_pallet || ""} ${w.bin_pallet_no || ""}`.trim();
                tbody.innerHTML += `<tr>
                    <td>${w.warehouse_name}</td>
                    <td>${w.rack}</td>
                    <td>${w.shelf}</td>
                    <td>${bp || "--"}</td>
                    <td class="blue-bold">${w.qty}</td>
                </tr>`;
            });
        } else {
            tbody.innerHTML = '<tr><td colspan="6" style="text-align:center;color:#888;">No warehouse data found.</td></tr>';
        }
        
        const uniqueWhs = new Set(p.warehouses.map(w => w.warehouse_name)).size;
        statusEl.innerText = `PRODUCT FOUND: ${p.name} | ${uniqueWhs} warehouse(s)`;
        statusEl.style.backgroundColor = "#10B981";
    } else {
        lastProductData = null;
        document.getElementById('det-pid').innerText = productId;
        document.getElementById('det-name').innerText = "NOT FOUND IN DATABASE";
        document.getElementById('det-cat').innerText = "--";
        document.getElementById('det-brand').innerText = "--";
        document.getElementById('det-batch').innerText = "--";
        document.getElementById('det-expiry').innerText = "--";
        document.getElementById('det-total-qty').innerText = "--";
        document.getElementById('det-balance-qty').innerText = "--";
        document.getElementById('det-qr').innerText = productId;
        document.getElementById('det-wh-body').innerHTML = '<tr><td colspan="6" style="text-align:center;color:#888;">No warehouse data found.</td></tr>';
        
        // Show the API error message if available
        const errMsg = data.message || 'ID not in database';
        statusEl.innerText = `SCAN SUCCESS: ${errMsg}`;
        statusEl.style.backgroundColor = "#F59E0B";
    }
}

