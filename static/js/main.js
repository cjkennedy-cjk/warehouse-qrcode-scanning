function previewBalance() {
    const current = parseInt(document.getElementById('gen-current-qty').value) || 0;
    const add = parseInt(document.getElementById('gen-add').value) || 0;
    const rem = parseInt(document.getElementById('gen-remove').value) || 0;
    
    let newWhQty = current + add - rem;
    if (newWhQty < 0) newWhQty = 0;
    
    const newTotal = dbTotalQty - dbCurrentWhQty + newWhQty;
    
    document.getElementById('gen-total').value = newWhQty;
    document.getElementById('gen-balance').value = newTotal;
}

function checkExistingLocationStock() {
    if (!currentProductWarehouses || currentProductWarehouses.length === 0) return;
    
    const wh = document.getElementById('gen-wh').value.trim().toLowerCase();
    
    const matched = currentProductWarehouses.find(w => 
        (w.warehouse_name || "").toLowerCase() === wh
    );
    
    if (matched) {
        document.getElementById('gen-current-qty').value = matched.qty;
        dbCurrentWhQty = parseInt(matched.qty) || 0;
    } else {
        document.getElementById('gen-current-qty').value = 0;
        dbCurrentWhQty = 0;
    }
    
    previewBalance();
}

