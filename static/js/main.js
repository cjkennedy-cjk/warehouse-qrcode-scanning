function filterActivityLogs() {
    const user = document.getElementById('log-user') ? document.getElementById('log-user').value : 'all';
    const activityType = document.getElementById('log-activity-type') ? document.getElementById('log-activity-type').value : 'all';
    const dateRange = document.getElementById('log-date-range') ? document.getElementById('log-date-range').value.trim() : '';
    
    let startDate = null;
    let endDate = null;
    if (dateRange.includes(' to ')) {
        const parts = dateRange.split(' to ');
        startDate = parts[0];
        endDate = parts[1];
    } else if (dateRange) {
        startDate = dateRange;
        endDate = dateRange;
    }

    filteredLogs = allLogs.filter(log => {
        if (user !== 'all' && log.username !== user) return false;
        
        let mappedType = '';
        if (log.action === 'LOGIN') mappedType = 'Login';
        else if (log.action === 'LOGOUT') mappedType = 'Logout';
        else if (log.action === 'ADD_PRODUCT' || log.action === 'UPDATE_PRODUCT') mappedType = 'Add';
        else if (log.action === 'REMOVE_PRODUCT') mappedType = 'Remove';
        
        if (activityType !== 'all' && mappedType !== activityType) return false;
        
        if (startDate && endDate && log.timestamp) {
            const logDate = log.timestamp.split(' ')[0];
            if (logDate < startDate || logDate > endDate) {
                return false;
            }
        } else if (dateRange && log.timestamp) {
            if (!log.timestamp.includes(dateRange)) return false;
        }
        
        return true;
    });
    
    logsCurrentPage = 1;
    renderActivityLogs();
}

function changeLogPage(delta) {
    const totalPages = Math.ceil(filteredLogs.length / logsPerPage);
    logsCurrentPage += delta;
    if (logsCurrentPage < 1) logsCurrentPage = 1;
    if (logsCurrentPage > totalPages) logsCurrentPage = totalPages || 1;
    renderActivityLogs();
}

